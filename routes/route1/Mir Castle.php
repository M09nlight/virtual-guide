                    construction in the Renaissance style.</p>
                <p class="text">The palace was surrounded by earth ramparts, bastions at the corners, and a moat. In the
                    northern part there was a beautiful Italian-style garden while in the southern part there was an
                    artificial pond. Its masonry resembles that of German churches. According to some legends, there
                    were underground passages leading to the Nesvizh Castle.
                    During the Napoleonic Wars, the palace was seriously damaged. It had undergone reconstruction at the
                    beginning of the 19th century and was later sold to Svyatopolk-Mirsky. His son continued
                    reconstruction led by a renowned Polish architect Teodor Bursche. The castle’s last owner prince
                    Mihail Svyatopolk-Mirsky mysteriously drowned in the moat.

                </p>
                <p class="text">In times of the Soviet Union, the palace served as a factory, and during the Second World War there was a ghetto and a war prisoner camp.</p>

