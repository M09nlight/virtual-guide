                <p class="text">Around 1730, the Kalinkovichi Orthodox parish became a union. In 1836, at a meeting of the parishioners of St. Nicholas Church, a decision was made to return from Uniatism to Orthodoxy. After the commissioning of a new stone church in 1856, a school was opened with it to teach peasant children to read and write. In 1930, the parish in Kalinkovichi was liquidated and the church of St. Nicholas was closed. Its building housed the local branch of the State Bank. The bell tower at the temple was dismantled, but the bells themselves and the removed domed crosses survived then.</p>
                <p class="text">During the Great Patriotic War, St. Nicholas Church was reopened. In 1964, on Radonitsa, immediately after the service, a commission of the city executive committee arrived at St. Nicholas Church, the chairman of which announced to the priest that the church was closed. The temple was immediately locked and sealed. Crosses were removed from it, domes were thrown off, the bells were handed over for scrap. Subsequently, the building was rebuilt and lost its church appearance. For a long time there was the city House of Pioneers, and now the House of Children's Creativity.</p>
