                <p class="text">According to legend, in 1757 Ivan Didko, a peasant from the village of Meleshkovichi, lost his oxen. He searched for them for a long time until he came to a small wooded hill ("Vaskovka Island"). Here, on a pear tree, he saw in an extraordinary radiance the icon of the Mother of God, and next to him - his missing oxen. On the site of the miraculous appearance of the icons, first a chapel was built, and then a wooden Vaskovskaya church.</p>
