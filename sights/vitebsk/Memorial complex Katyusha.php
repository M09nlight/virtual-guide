                <p class="text">In honor of this feat, in 1966, the Katyusha memorial complex was built in Orsha. Its
                    authors were well-known Belarusian masters Leonid Levin, Yuri Gradov, Valentin Zankovich. By the
                    way, this was not the last creative union of architects. It was they who developed the world-famous
                    Khatyn complex, for which they later received the Lenin Prize.</p>
                <p class="text">The monument is a model of a rocket-propelled mortar; in its manufacture, all the
