                    8 seconds (this is how long the salvo lasted), the enemy was completely defeated, and all fascist
                    military equipment was destroyed. The offensive along the Moscow-Smolensk direction was stopped for
                    a week, which allowed the Soviet troops to regroup their forces and strengthen their positions.</p>
                <p class="text">Unfortunately, a little later, Flerov's battery was ambushed. Almost the entire
                    personnel, led by the commander, died defending the secret of the rocket launcher. The Katyusha
                    itself was blown up.</p>
